"""### Manipulación de Dimensiones y Dispositivos

| Método | Descripción | Utilidad Principal |
| :--- | :--- | :--- |
| `reshape(shape)` | Cambia la forma del tensor sin cambiar sus datos. | Adaptar la salida de una capa a la entrada de otra (ej. de Conv a Linear). |
| `unsqueeze(dim)` | Añade una dimensión extra de tamaño 1 en la posición indicada. | Simular un 'batch size' de 1 o añadir canales a una imagen. |
| `squeeze(dim)` | Elimina dimensiones de tamaño 1. | Limpiar dimensiones innecesarias tras una operación. |
| `flatten()` | Aplana el tensor a una sola dimensión (vector). | Preparar datos para capas de clasificación finales. |
| `to(device)` | Mueve el tensor al dispositivo especificado (CPU o GPU). | **Crucial** para acelerar cálculos usando la tarjeta gráfica (CUDA). |
"""