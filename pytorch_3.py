"""### Descripción y Utilidad de los Tipos de Tensores

| Tipo de Tensor | Clase de PyTorch | Descripción | Utilidad Principal |
| :--- | :--- | :--- | :--- |
| **Float** | `tc.FloatTensor` | Punto flotante de 32 bits. | Es el estándar para pesos de redes neuronales y cálculos de precisión simple. |
| **Double** | `tc.DoubleTensor` | Punto flotante de 64 bits. | Se usa cuando se requiere una precisión matemática extrema (cálculos científicos). |
| **Half** | `tc.HalfTensor` | Punto flotante de 16 bits. | Ideal para entrenamiento de 'precisión mixta' para ahorrar memoria y ganar velocidad en GPUs modernas. |
| **Byte** | `tc.ByteTensor` | Enteros de 8 bits sin signo (0 a 255). | Comúnmente usado para máscaras booleanas (aunque ahora existe `BoolTensor`) o datos de imágenes. |
| **Char** | `tc.CharTensor` | Enteros de 8 bits con signo (-128 a 127). | Útil para almacenamiento de datos muy pequeños y cuantización. |
| **Short** | `tc.ShortTensor` | Enteros de 16 bits con signo. | Ahorro de memoria cuando los valores no superan los +/- 32,767. |
| **Int** | `tc.IntTensor` | Enteros de 32 bits con signo. | Usado para índices o conteos que no requieren el rango de 64 bits. |
| **Long** | `tc.LongTensor` | Enteros de 64 bits con signo. | **Fundamental** para etiquetas de clasificación (targets) e índices de búsqueda (embeddings). |
"""

x =tc.zeros(4,3,dtype=tc.long)
print(x)
x= tc.ones(4,3,dtype=tc.long)
print(x)
x=x.new_ones(4,3,dtype=tc.double)
print(x)
x=tc.randn_like(x,dtype=tc.float)
print(x)

"""### Métodos de Creación y Transformación de Tensores

| Método | Descripción | Utilidad Principal |
| :--- | :--- | :--- |
| `tc.zeros(shape)` | Crea un tensor lleno de ceros con la forma especificada. | Inicialización de estados, acumuladores o padding. |
| `tc.ones(shape)` | Crea un tensor lleno de unos con la forma especificada. | Inicialización de constantes o máscaras unitarias. |
| `tensor.new_ones(shape)` | Crea un nuevo tensor de unos **reutilizando atributos** (como el dispositivo) del tensor original. | Mantener la consistencia del dispositivo (CPU/GPU) al crear nuevos datos. |
| `tc.randn_like(tensor)` | Crea un tensor con valores aleatorios (distribución normal) con la **misma forma** que el tensor dado. | Generación de ruido o inicialización aleatoria basada en un tensor de referencia. |
"""

x =tc.Tensor(np.array([[1,2],[3,4]]))
