def obtener_respuesta(texto_usuario, model, dataset, respuestas, threshold=0.35):


    model.eval()

    # Convertir texto a vector
    vector = dataset._frase_a_vector(texto_usuario)
    X = torch.tensor(vector, dtype=torch.float32).unsqueeze(0).to(device)

    # Predecir
    with torch.no_grad():
        outputs = model(X)
        probabilities = torch.softmax(outputs, dim=1)
        confianza, idx = torch.max(probabilities, 1)

    confianza = confianza.item()
    idx = idx.item()

    # Decodificar intención
    intención = dataset.label_encoder.inverse_transform([idx])[0]

    # Threshold adaptativo
    if confianza < threshold:
        intención = "no_entendí"

    # Seleccionar respuesta aleatoria
    respuesta = random.choice(respuestas[intención])

    return respuesta, confianza, intención

