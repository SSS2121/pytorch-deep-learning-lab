class ChatDataset(Dataset):
    def __init__(self, intenciones):
        self.intenciones = intenciones
        self.todas_las_palabras = []
        self.tags = []
        self.xy = []

        for tag, frases in intenciones.items():
            self.tags.append(tag)
            for frase in frases:
                frase_normalizada = self._normalizar_texto(frase)
                w = self._tokenizar(frase_normalizada)
                self.todas_las_palabras.extend(w)
                self.xy.append((w, tag))

        self.ignorar_palabras = ['?', '!', '.', ',']
        self.todas_las_palabras = sorted(list(set([w for w in self.todas_las_palabras if w not in self.ignorar_palabras])))
        self.vocab = self.todas_las_palabras

        self.label_encoder = LabelEncoder()
        self.labels = self.label_encoder.fit_transform(self.tags)

    def __len__(self):
        return len(self.xy)

    def __getitem__(self, index):
        frase, tag = self.xy[index]
        vector = self._frase_a_vector(frase)
        label = self.label_encoder.transform([tag])[0]
        return torch.tensor(vector, dtype=torch.float32), torch.tensor(label, dtype=torch.long)

    def _tokenizar(self, texto):
        return texto.lower().split()

    def _normalizar_texto(self, texto):
        texto = ''.join(c for c in texto if c.isalnum() or c.isspace() or c in ['?', '!', '.', ','])
        texto = unicodedata.normalize('NFKD', texto).encode('ascii', 'ignore').decode('utf-8', 'ignore')
        return texto.lower()

    def _frase_a_vector(self, tokenized_sentence):
        bag = np.zeros(len(self.vocab), dtype=np.float32)
        for idx, w in enumerate(self.vocab):
            if w in tokenized_sentence:
                bag[idx] = 1
        return bag

class ChatBot(nn.Module):
    """Red neuronal mejorada para clasificar intenciones"""

    def __init__(self, vocab_size, num_intenciones, hidden_size=256):
        super(ChatBot, self).__init__()

        # Capas más profundas
        self.fc1 = nn.Linear(vocab_size, hidden_size)
        self.bn1 = nn.BatchNorm1d(hidden_size)
        self.relu1 = nn.ReLU()
        self.dropout1 = nn.Dropout(0.4)

        self.fc2 = nn.Linear(hidden_size, 128)
        self.bn2 = nn.BatchNorm1d(128)
        self.relu2 = nn.ReLU()
        self.dropout2 = nn.Dropout(0.4)

        self.fc3 = nn.Linear(128, 64)
        self.relu3 = nn.ReLU()
        self.dropout3 = nn.Dropout(0.3)

        self.fc4 = nn.Linear(64, num_intenciones)

    def forward(self, x):
        x = self.fc1(x)
        x = self.bn1(x)
        x = self.relu1(x)
        x = self.dropout1(x)

        x = self.fc2(x)
        x = self.bn2(x)
        x = self.relu2(x)
        x = self.dropout2(x)

        x = self.fc3(x)
        x = self.relu3(x)
        x = self.dropout3(x)

        x = self.fc4(x)
        return x

