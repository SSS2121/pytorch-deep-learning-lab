# Loss y optimizer
criterion = nn.CrossEntropyLoss()
optimizer = optim.AdamW(model.parameters(), lr=0.002, weight_decay=0.0001)
scheduler = torch.optim.lr_scheduler.StepLR(optimizer, step_size=50, gamma=0.5)

# Entrenamiento
num_epochs = 80
best_loss = float('inf')
best_accuracy = 0

print("Entrenamiento (80 épocas)...\n")

for epoch in range(num_epochs):
    model.train()
    total_loss = 0
    correct = 0
    total = 0

    for X, y in train_loader:
        X, y = X.to(device), y.to(device)

        # Forward
        outputs = model(X)
        loss = criterion(outputs, y)

        # Backward
        optimizer.zero_grad()
        loss.backward()
        torch.nn.utils.clip_grad_norm_(model.parameters(), 1.0)
        optimizer.step()

        # Estadísticas
        total_loss += loss.item()
        _, predicted = torch.max(outputs, 1)
        correct += (predicted == y).sum().item()
        total += y.size(0)

    scheduler.step()

    accuracy = 100 * correct / total
    avg_loss = total_loss / len(train_loader)

    # Guardar mejor modelo
    if avg_loss < best_loss or accuracy > best_accuracy:
        best_loss = avg_loss
        best_accuracy = accuracy
        torch.save(model.state_dict(), 'chatbot_model_mejorado.pth')

    # Imprimir cada 30 épocas
    if (epoch + 1) % 30 == 0:
        print(f"Epoch [{epoch+1}/{num_epochs}] Loss: {avg_loss:.4f} Accuracy: {accuracy:.2f}%")

print(f"\n Entrenamiento completado")
